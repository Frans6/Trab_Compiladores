# Teste de precedência de operadores

# Precedência básica: multiplicação antes de adição
resultado1 = 2 + 3 * 4

# Precedência com divisão
resultado2 = 10 / 2 + 3

# Precedência com parênteses
resultado3 = (2 + 3) * 4

# Precedência com subtração
resultado4 = 10 - 3 + 2

# Operações simples
resultado5 = 5 + 3 * 2
resultado6 = (5 + 3) * 2

# Múltiplas operações
resultado7 = 2 + 3 * 4 - 1

print(resultado1)
print(resultado2)
print(resultado3)
print(resultado4)
print(resultado5)
